package com.bquarkz.hellskitchen.fsm;

import java.io.Serializable;

public interface IFSMNavigation< ID extends Serializable, T >
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Static fields
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Static Methods
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Default Methods
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    default IAction< ID, T > goTo( ID id ) throws FSMTransitionException, FSMMachineNotRunningException
    {
        return goTo( id, ( current, next ) -> next );
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Contracts
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    IState< ID, T > getCurrentState();
    IAction< ID, T > goTo( ID id, IFSMTransition< T > transition ) throws FSMTransitionException, FSMMachineNotRunningException;
}
